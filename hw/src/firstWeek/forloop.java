package firstWeek;

public class forloop {

	public static void main(String[] args) {
		//For
		for(int i=1;i<=10;i++) {
			System.out.println(i);
			
		}
		System.out.println("Birer arttirma dongusu bitti.");
		
		for(int i=1;i<=10;i+=2) {
			System.out.println(i);
			
		}
		System.out.println("Ikiser arttirma dongusu bitti.");
		
		for(int i=0;i<10;i+=2) {
			System.out.println(i);
			
		}
		System.out.println("0-10 arasi cift sayi dongusu bitti.");
		

	}

}
