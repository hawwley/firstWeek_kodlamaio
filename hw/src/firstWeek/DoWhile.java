package firstWeek;

public class DoWhile {

	public static void main(String[] args) {
		int i=1;
		do {
			System.out.println(i);
			i++;  //i+=2 veya i=+2
		}while (i<10); 
		System.out.println("Do-While Dongusu Bitti.");
		
		
		int j=100;  //100'u yazdirir,sonra sarti kontrol eder.
		do {
			System.out.println("Loglandi.");
			System.out.println(j);
			j++;  //i+=2 veya i=+2
		}while (j<10); 
		System.out.println("Do-While Dongusu Bitti.");
		
		//Dongu calismasa bile dongunun calistigina dair bir deneme yapmak icin kullanilabilir.
			
		
		
		
		
		
	}

}
